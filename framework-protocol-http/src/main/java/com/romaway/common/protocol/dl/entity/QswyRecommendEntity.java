public class QswyRecommendEntity {

    private String id;
    private String title;
    private String day;
    private String in_come;
    private String in_num;
    private String time_type;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getIn_come() {
        return in_come;
    }

    public void setIn_come(String in_come) {
        this.in_come = in_come;
    }

    public String getIn_num() {
        return in_num;
    }

    public void setIn_num(String in_num) {
        this.in_num = in_num;
    }

    public String getTime_type() {
        return time_type;
    }

    public void setTime_type(String time_type) {
        this.time_type = time_type;
    }
}
